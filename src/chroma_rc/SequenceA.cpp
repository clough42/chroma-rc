int SequenceA::advance(void) {
	if( pos >= sizeof(data)/sizeof(data[0]) ) {
		pos = 0;
	}

	for( byte i=0; i < rgb_pins_size; i++ ) {
		digitalWrite(rgb_pins[i], data[pos][i]);
	}

	pos++;

	return 100;
}
